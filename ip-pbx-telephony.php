                            <ul class="list-style-type">
                                <li><h3>Types of PBX</h3></li>
                                <li><h4>1. Traditional PBX</h4></li>
                                <li><p>A traditional PBX connects callers through limited phone lines to multiple extensions. A digital PBX system is just an upgrade that can house a traditional analog PBX system, as well as IP (Internet Protocol) phones. An IP PBX is good for businesses that want direct control over exactly how their PBX is configured in-house, and not want to pay subscription fees associated with other PBX options.</p></li>
                            </ul>
                        </div>
                        <div class="col-md-12">
                            <ul class="list-style-type">
                                <li><h4>2. VoIP or On-site PBX</h4></li>
                                <li><p>Voice over Internet Protocol (VoIP) Private Branch Exchange (PBX) is an on-site internet protocol which can transmit phone calls from the traditional fixed or mobile network to the Internet and vice versa using the existing robust internet connections readily available.</p></li>
                                <li><strong>The benefits of a VoIP PBX for an SME</strong></li>
                                <li><p><strong>Easy installation:</strong> in comparison to the installation of a traditional switchboard, VoIP telephone switchboards are much simpler to install</p></li>
                                <li><p><strong>Scalability:</strong> in a traditional switchboard, you would need to know if you have space in the physical 'switchboard' to add additional lines or you must buy a new one and install it that adds to the cost. In a software-based VoIP PBX, you just need to expand the memory of your PBX</p></li>
                                <li><p><strong>Cheaper calls:</strong> the savings in long distance calls can be greater, especially if made outside the country.</p></li>
                                <li><p><strong>Easy to repair:</strong> Problems are easier to solve because they tend to be software related.</p></li>
                                <li><p><strong>Value added services:</strong> the traditional telephone exchanges donâ€™t have added services like the answering machine or a phone book. The VoIP PBX not only has answering machine and call waiting, but can be synchronized with the CRM of the company, calendar, or Cloud</p></li>
                                <li><p><strong>Recycle:</strong> VoIP PBXs allow conventional digital phones to be reused with just one adapter, in addition to being able to use modern terminals that are in themselves small computers, with all the added value services a business expects.</p></li>
                            </ul>
                        </div>
                        <div class="col-md-12">
                            <ul class="list-style-type">
                                <li><h4>3. Virtual or Hosted PBX</h4></li>
                                <li><p>A virtual PBX system also known as Cloud PBX or Hosted PBX, provides all PBX system features and services in the cloud. There are no software updates or PBX maintenance or warranties to be concerned as the telecom service provider hosts it on a virtual phone system. A hosted VoIP PBX is a popular choice for many businesses</p></li>
                            </ul>
                        </div>
                        <div class="col-md-12">
